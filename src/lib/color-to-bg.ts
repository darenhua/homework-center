export const mapColorToBg = (color: string) => {
    switch (color) {
        case "red":
            return "bg-red-200";
        case "green":
            return "bg-green-200";
        case "blue":
            return "bg-blue-200";
        case "purple":
            return "bg-purple-200";
        case "orange":
            return "bg-orange-200";
        case "pink":
            return "bg-pink-200";
        case "brown":
            return "bg-brown-200";
        case "yellow":
            return "bg-yellow-200";
    }
};
